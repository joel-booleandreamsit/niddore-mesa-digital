import { cookies } from "next/headers"

export type Lang = "pt" | "en"

export async function getLang(): Promise<Lang> {
  try {
    const c = (await cookies()).get("lang")?.value
    return c === "en" ? "en" : "pt"
  } catch {
    return "pt"
  }
}

